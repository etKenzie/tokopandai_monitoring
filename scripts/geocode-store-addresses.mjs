/**
 * One-time import: geocode store addresses and write JSON + MySQL SQL.
 *
 * Usage:
 *   1. Copy your address list to scripts/input/store-addresses.json
 *   2. npm run geocode:stores
 *   3. Create table: migrations/mysql/store_address_geocodes.sql
 *   4. Import rows: scripts/output/store_address_geocodes.sql
 *      OR use data/store-address-geocodes.json (Next.js app reads this directly)
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const INPUT_PATH = path.join(__dirname, 'input', 'store-addresses.json');
const OUTPUT_JSON_PATH = path.join(__dirname, '..', 'data', 'store-address-geocodes.json');
const OUTPUT_SQL_PATH = path.join(__dirname, 'output', 'store_address_geocodes.sql');

const serverCache = new Map();
let lastNominatimRequest = 0;

const sleep = (ms) => new Promise((resolve) => setTimeout(resolve, ms));

function normalizeAddress(address) {
  return address.trim().replace(/\s+/g, ' ').toLowerCase();
}

function escapeSql(value) {
  return value.replace(/'/g, "''");
}

async function waitForNominatimSlot() {
  const elapsed = Date.now() - lastNominatimRequest;
  if (elapsed < 1100) await sleep(1100 - elapsed);
  lastNominatimRequest = Date.now();
}

async function geocodeWithPhoton(address) {
  try {
    const response = await fetch(
      `https://photon.komoot.io/api/?q=${encodeURIComponent(address)}&limit=1&lang=en`,
      { headers: { Accept: 'application/json' } }
    );
    if (!response.ok) return null;
    const data = await response.json();
    const coordinates = data.features?.[0]?.geometry?.coordinates;
    if (!Array.isArray(coordinates) || coordinates.length < 2) return null;
    return { lat: coordinates[1], lng: coordinates[0] };
  } catch {
    return null;
  }
}

async function geocodeWithNominatim(address) {
  await waitForNominatimSlot();
  try {
    const response = await fetch(
      `https://nominatim.openstreetmap.org/search?format=json&limit=1&q=${encodeURIComponent(address)}`,
      {
        headers: {
          'User-Agent': 'TokopandaiMonitoring/1.0 (geocode-import)',
          Accept: 'application/json',
        },
      }
    );
    if (!response.ok) return null;
    const data = await response.json();
    if (!Array.isArray(data) || !data[0]?.lat || !data[0]?.lon) return null;
    return { lat: parseFloat(data[0].lat), lng: parseFloat(data[0].lon) };
  } catch {
    return null;
  }
}

async function geocodeAddress(address) {
  const normalized = normalizeAddress(address);
  if (!normalized) return null;
  if (serverCache.has(normalized)) return serverCache.get(normalized);

  let result = await geocodeWithPhoton(address);
  if (!result) result = await geocodeWithNominatim(address);

  serverCache.set(normalized, result);
  return result;
}

function loadInput() {
  if (!fs.existsSync(INPUT_PATH)) {
    console.error(`Missing input file: ${INPUT_PATH}`);
    console.error('Copy scripts/input/store-addresses.example.json to store-addresses.json and add your addresses.');
    process.exit(1);
  }

  const raw = JSON.parse(fs.readFileSync(INPUT_PATH, 'utf-8'));
  if (!Array.isArray(raw)) {
    throw new Error('Input must be a JSON array');
  }

  return raw
    .map((item) => {
      if (typeof item === 'string') {
        return { user_id: null, store_name: null, address: item };
      }
      return {
        user_id: item.user_id ?? null,
        store_name: item.store_name ?? null,
        address: item.address?.trim() ?? '',
      };
    })
    .filter((item) => item.address);
}

async function main() {
  const rows = loadInput();
  const uniqueByAddress = new Map();

  rows.forEach((row) => {
    const key = normalizeAddress(row.address);
    if (!uniqueByAddress.has(key)) {
      uniqueByAddress.set(key, row);
    }
  });

  const existing = fs.existsSync(OUTPUT_JSON_PATH)
    ? JSON.parse(fs.readFileSync(OUTPUT_JSON_PATH, 'utf-8'))
    : {};

  const outputJson = { ...existing };
  const sqlLines = [
    '-- Generated by scripts/geocode-store-addresses.mjs (MySQL)',
    '-- Prerequisite: run migrations/mysql/store_address_geocodes.sql first',
    '',
    'INSERT INTO store_address_geocodes (user_id, store_name, address_normalized, address_raw, latitude, longitude, geocode_source)',
    'VALUES',
  ];

  const sqlValues = [];
  let geocoded = 0;
  let skipped = 0;
  let failed = 0;
  const total = uniqueByAddress.size;
  let index = 0;

  for (const [normalized, row] of uniqueByAddress.entries()) {
    index += 1;
    if (outputJson[normalized]?.lat != null && outputJson[normalized]?.lng != null) {
      skipped += 1;
      console.log(`[${index}/${total}] skip (cached): ${row.store_name ?? row.address.slice(0, 40)}...`);
      continue;
    }

    process.stdout.write(`[${index}/${total}] geocoding: ${row.store_name ?? row.address.slice(0, 50)}... `);
    const coords = await geocodeAddress(row.address);

    if (!coords) {
      failed += 1;
      console.log('FAILED');
      continue;
    }

    geocoded += 1;
    console.log(`OK (${coords.lat}, ${coords.lng})`);

    outputJson[normalized] = {
      lat: coords.lat,
      lng: coords.lng,
      address_raw: row.address,
      user_id: row.user_id,
      store_name: row.store_name,
    };

    const userIdSql = row.user_id ? `'${escapeSql(row.user_id)}'` : 'NULL';
    const storeNameSql = row.store_name ? `'${escapeSql(row.store_name)}'` : 'NULL';

    sqlValues.push(
      `(${userIdSql}, ${storeNameSql}, '${escapeSql(normalized)}', '${escapeSql(row.address)}', ${coords.lat}, ${coords.lng}, 'import')`
    );

    fs.mkdirSync(path.dirname(OUTPUT_JSON_PATH), { recursive: true });
    fs.writeFileSync(OUTPUT_JSON_PATH, JSON.stringify(outputJson, null, 2));
  }

  if (sqlValues.length > 0) {
    sqlLines.push(sqlValues.join(',\n'));
    sqlLines.push('ON DUPLICATE KEY UPDATE');
    sqlLines.push('  latitude = VALUES(latitude),');
    sqlLines.push('  longitude = VALUES(longitude),');
    sqlLines.push('  address_raw = VALUES(address_raw),');
    sqlLines.push('  store_name = VALUES(store_name),');
    sqlLines.push('  user_id = VALUES(user_id),');
    sqlLines.push('  updated_at = CURRENT_TIMESTAMP;');

    fs.mkdirSync(path.dirname(OUTPUT_SQL_PATH), { recursive: true });
    fs.writeFileSync(OUTPUT_SQL_PATH, `${sqlLines.join('\n')}\n`);
  }

  console.log('\nDone.');
  console.log(`  Geocoded: ${geocoded}`);
  console.log(`  Skipped (already cached): ${skipped}`);
  console.log(`  Failed: ${failed}`);
  console.log(`  JSON: ${OUTPUT_JSON_PATH}`);
  if (sqlValues.length > 0) {
    console.log(`  SQL:  ${OUTPUT_SQL_PATH}`);
  }
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
